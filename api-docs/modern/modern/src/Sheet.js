/**
 * @class Ext.Sheet
 * @extend Ext.Panel
 * @xtype sheet
 *
 * A general sheet class. This renderable container provides base support for
 * orientation-aware transitions for popup or
 * side-anchored sliding Panels.
 *
 * In most cases, you should use {@link Ext.ActionSheet}, {@link Ext.MessageBox},
 * {@link Ext.picker.Picker}, or {@link Ext.picker.Date}.
 */

/**
 * @cfg [modal=true]
 * @inheritdoc
 */

/**
 * @cfg {Boolean} [centered=true]
 * Whether or not this component is absolutely centered inside its container.
 * @accessor
 * @evented
 */

/**
 * @cfg {Boolean} [stretchX=null]
 * `true` to stretch this sheet horizontally.
 */

/**
 * @cfg {Boolean} [stretchY=null]
 * `true` to stretch this sheet vertically.
 */

/**
 * @cfg {String} [enter=bottom]
 * The viewport side used as the enter point when shown. Valid values are 'top', 'bottom', 'left', and 'right'.
 * Applies to sliding animation effects only.
 */

/**
 * @cfg {String} [exit=bottom]
 * The viewport side used as the exit point when hidden. Valid values are 'top', 'bottom', 'left', and 'right'.
 * Applies to sliding animation effects only.
 */

/**
 * @cfg showAnimation
 * @inheritdoc
 */

/**
 * @cfg hideAnimation
 * @inheritdoc
 */
