/**
  *
  * @class Ext.dd.ScrollManager
  * @extend Ext.Base
  * @singleton
  * @private
  *
*/

/**
 * @method scrollTowardsPointer
 * Register scroller task while dragging on component
 * scroll the view towards the pointer.
 * @param component
 */

/**
 * @method stopAutoScroller
 * Remove scroller task
 */

/**
 * @method doAutoScroll
 * Scroll component to next visible area
 */

/**
 * @method getScrollDirection
 * Checks current cursor position and
 * return `-1`, `0` or `1`
 *
 * `-1` - scroll up
 * `0`  - no scroll
 * `1`  - scroll down
 */
